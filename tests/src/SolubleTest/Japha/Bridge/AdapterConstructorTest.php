        ]);
    }

    #[Test]
    public function constructorSetsCustomDefaultTimeZone(): void
    {
        $ba = new Adapter([
            'driver' => 'Pjb62',
            'servlet_address' => $this->servlet_address,
            'java_default_timezone' => 'Europe/London'
        ]);
        $javaTz = $ba->getSystem()->getTimeZoneId();
        $this->assertSame('Europe/London', $javaTz);
    }

    #[Test]
    public function constructorWithDefaultriver(): void
    {
        $ba = new Adapter([
            'servlet_address' => $this->servlet_address,
        ]);

        $driverClass = $ba->getDriver()::class;
        $this->assertSame(Pjb62Driver::class, $driverClass);
    }

